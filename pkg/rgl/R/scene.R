rgl.clear <- function( type = "shapes" )
{
  type <- rgl.enum.nodetype(type)
  
  viewpoint <- 4 %in% type
  material  <- 5 %in% type

  type <- type[!(type %in% 4:6)]
  
  idata <- as.integer(c(length(type), type))
 
  ret <- .C( rgl_clear, 
    success = FALSE,
    idata
  )
  
  if ( viewpoint ) 
    rgl.viewpoint()
    
  if ( material ) 
    rgl.material()

  if (! ret$success)
    stop("rgl_clear")
}


